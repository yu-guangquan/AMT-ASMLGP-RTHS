AdaptiveRTHS = zeros(nRow,1);
TotalTrainingRTHS = zeros(nRow,1);
IndependentValidationRTHS = N_val * ones(nRow,1);
FinalRMSE = nan(nRow,1);
FinalR2 = nan(nRow,1);

for i = 1:nRow

    Method(i) = string(rows{i,1});
    MetaTaskSetting(i) = string(rows{i,2});

    fileName = rows{i,3};
    rmseVar  = rows{i,4};
    r2Var    = rows{i,5};
    nAdapt   = rows{i,6};

    AdaptiveRTHS(i) = nAdapt;
    TotalTrainingRTHS(i) = N_init + nAdapt;

    if ~exist(fileName, 'file')
        warning('File not found: %s', fileName);
        continue;
    end

    S = load(fileName);

    if ~isfield(S, rmseVar)
        warning('Variable %s not found in %s', rmseVar, fileName);
    else
        rmseData = S.(rmseVar);
        FinalRMSE(i) = get_final_value(rmseData, nAdapt);
    end

    if ~isfield(S, r2Var)
        warning('Variable %s not found in %s', r2Var, fileName);
    else
        r2Data = S.(r2Var);
        FinalR2(i) = get_final_value(r2Data, nAdapt);
    end
end

