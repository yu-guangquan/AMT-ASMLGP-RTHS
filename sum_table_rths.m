% Local function
%% ------------------------------------------------------------
function val = get_final_value(data, nAdapt)
    data = data(:);

    if isempty(data)
        val = NaN;
        return;
    end

    % Use the specified convergence/adaptive index when available.
    if numel(data) >= nAdapt
        val = data(nAdapt);
    else
        % Fall back to the last available value.
        val = data(end);
        warning('Data length is smaller than nAdapt. The last available value is used.');
    end
end